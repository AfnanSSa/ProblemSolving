//Write a program that converts inches to centimeters and prints the result.

public class ProblemSolving {
    public static void main(String[] args) {
        // Define inch value
        double inches = 2; 
        
        // Convert to centimeters
        double centimeters = inches * 2.54;

        //Print result
        System.out.println(inches + " inches are equal to " + centimeters + " cm");
       
    }
}